function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

function focusColor(colorIndex, palette) {
  let colorZone = palette.children[colorIndex];
  colorZone.click();
}

async function color_pixel(x, y, color, grid, palette) {
  //console.log("Coloring pixel (", x, ",", y, ") in color n°", color);
  focusColor(color, palette);
  let pixel = grid.children[x + y * 100];
  let c = pixel.style.backgroundColor;
  pixel.click();
  await sleep(100);
  if (pixel.style.backgroundColor == c) {
    pixel.style.backgroundColor = "red";
    await sleep(100);
    pixel.style.backgroundColor = "white";
    await sleep(100);
    pixel.style.backgroundColor = c;
  }
}

function getPixelColorIndex(x, y, grid, paletteCorrespondance) {
  let pixel = grid.children[x + y * 100];
  for (let i = 0; i < 15; i++) {
    if (paletteCorrespondance[i] == pixel.style.backgroundColor) {
      return i;
    }
  }
  //console.log("No correspondance found");
  return -1;
}

function match(
  x1,
  y1,
  template,
  grid,
  palette,
  paletteCorrespondance,
  deterministic
) {
  let det = deterministic == null ? false : deterministic;
  // Matches the zone to the template
  let non_conform = [];
  let x2 = x1 + template[0].length;
  let y2 = y1 + template.length;
  for (let i = 0; i < x2 - x1; i++) {
    for (let j = 0; j < y2 - y1; j++) {
      let c = getPixelColorIndex(i + x1, j + y1, grid, paletteCorrespondance);
      if (c != template[j][i] && template[j][i] >= 0) {
        non_conform.push([i, j]);
      }
    }
  }
  let i = det ? 0 : Math.floor(Math.random() * non_conform.length);
  let p = 100 - (non_conform.length / ((x2 - x1) * (y2 - y1))) * 100;
  if (p < 100) {
    console.log(
      `${p}% completion, ${Math.floor(non_conform.length / 60)}h${
        non_conform.length % 60
      } remaining (${non_conform.length} pixels)`
    );
  }
  if (non_conform.length > 0) {
    let e = non_conform[i];
    color_pixel(e[0] + x1, e[1] + y1, template[e[1]][e[0]], grid, palette);
    return true;
  }
  return false;
}

